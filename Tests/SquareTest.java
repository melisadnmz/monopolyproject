import org.junit.Test;

import static org.junit.Assert.assertEquals;

public class SquareTest {

    private Square square = new Square("TestSquareName", "Type" , 0 , 100 );

    @Test
    public void setName(){
        square.setName("setSquareName");
        assertEquals("setSquareName",square.getName());
    }

    @Test
    public void getName(){
        assertEquals("TestSquareName",square.getName());
    }

    @Test
    public void setType(){
        square.setType("setType");
        assertEquals("setType",square.getType());
    }

    @Test
    public void getType(){
        assertEquals("Type",square.getType());
    }

    @Test
    public void setIndex(){
        square.setIndex(0);
        assertEquals(0,square.getIndex());
    }

    @Test
    public void getIndex(){
        assertEquals(0,square.getIndex());
    }

    @Test
    public void setFee(){
        square.setFee(0);
        assertEquals(0,square.getFee(),0);
    }

    @Test
    public void getFee(){
        assertEquals(100,square.getFee(),0);
    }
}
